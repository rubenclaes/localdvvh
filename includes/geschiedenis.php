    <h1>Geschiedenis </h1>
    <hr>
    <div class="row-fluid">
        
            <img class="pull-left img-polaroid" src="images/1.jpg" width="450" height="290" id="figuur1" alt="figuur1"/>
        
            <p> In het jaar 1902 besluiten op een augustusavond 
            de gemeentesecretaris Pieter Jan Lemmens en de koster Adamus 
            Theodorus Reynders een fanfare op te richten. Zij kloppen aan 
            bij de burgemeester, graaf Albert de Theux de Meylandt et de 
            Montjardin, om steun te vragen bij de oprichting van een 
            muziekmaatschappij onder de naam &QUOT;De Verenigde Vrienden&QUOT;. 
            Graaf de Theux wordt tot erevoorzitter benoemd en geeft direct 
            de broodnodige financi&euml;le steun. De ledenwerving 
            verloopt voorspoedig : 29 mannen melden zich aan 
            en nemen de verplichting op zich door 
            maandelijkse betalingen hun instrumenten zelf te bekostigen. 
            De allereerste lessen worden gegeven door koster Reynders 
            in het werkhuis van kuiper Beenaerts. Kort daarop wordt 
            beroep gedaan op de heer Saillart, muziekleraar 
            uit Leopoldsburg. Onder zijn leiding vinden 
            de repetities plaats en zodoende kan de fanfare 
            reeds op paasmaandag van 1903 een muzikale opluistering 
            geven ter gelegenheid van de eremis van E.H. Franken, broer van de 
            pastoor. Tevens trekt vanaf die tijd de fanfare mee op in de twee 
            processies die dan in Heusden nog uitgaan. Op Sint-Janskermis 
            wordt een eerste concert gegeven.</p>

        <p>
    </div><br>
    <div class="row-fluid">
        <img class="pull-right img-polaroid" src="images/2.jpg" width="450" height="321" id="figuur2" alt="figuur2"/>
        In 1905 wordt herdacht dat Belgi&euml; 75 jaar vroeger onafhankelijk was 
        geworden. De fanfare verzorgt tijdens deze feesten een drietal 
        concerten en trekt mee in de stoet te Hasselt. 
        Onder leiding van Alfons Roosen, die de heer Saillart is opgevolgd als 
        dirigent, neemt de fanfare onder meer deel aan muziekfestivals te 
        Beverlo, Heppen en Houthalen. De eerste wereldoorlog dwingt de fanfare 
        tot werkloosheid. De Duitsers eisen namelijk alle koper op, maar de 
        bezetters slagen er niet in ook maar &eacute;&eacute;n instrument te vinden. Na de 
        wapenstilstand komen dan ook de instrumenten weer te voorschijn. 
        Meester Leon Boonen wordt enige tijd dirigent. Rond 1920 wordt een 
        nieuw bestuur gekozen met Jan Steenaerts als voorzitter en Maurice 
        Timmermans als secretaris. Als dirigent treedt Louis Melis uit Beringen 
        op. Het aanleren van een groep jonge muzikanten neemt Paul Mertens op 
        zich en weldra telt de fanfare opnieuw 28 spelende leden. 
        Het opluisteren van vaderlandse feesten, processies en andere activiteiten, 
        kan verder gaan. In die periode van de twintiger jaren is de fanfare 
        niet alleen muzikaal actief, maar is zij ook bedrijvig met het 
        inrichten van jaarlijkse toneelvoorstellingen.</p>

        <p>
    </div><br>
    <div class="row-fluid">
        <img class="pull-left img-polaroid" src="images/3.jpg" width="450" height="254" id="figuur3" alt="figuur3" />
        Tijdens de Tweede Wereldoorlog moeten de instrumenten weer 
        verdwijnen en vallen alle activiteiten stil. Maar na de 
        bevrijding in september 1944 herrijst de muziekvereniging 
        onder leiding van Albert Beckers - al voor de oorlog secretaris 
        van de fanfare geworden - en de onderwijzers Theo Peeters 
        en Henri Meerten. Instrumenten worden ingezameld en gerepareerd.
        Tandarts Jos Lemmens, zoon van stichter Pieter Jan Lemmens, 
        neemt het voorzitterschap op zich. Dirigent is Alfons Deferme, 
        die enkele jaren voor de oorlog Louis Melis is opgevolgd. 
        Een heuglijke dag voor de fanfare De Verenigde Vrienden 
        is 19 februari 1952, omdat zij voortaan de titel 
        &QUOT;Koninklijke Maatschappij&QUOT; mag voeren. 
        Ander positief nieuws uit die jaren 
        is dat in 1954 wordt deelgenomen aan het Wereld Muziek Concours 
        in Kerkrade (Nederland), waar een tweede prijs wordt behaald. 
        Een donkere dag in de geschiedenis van de fanfare is het concert
        te Houthalen in de zomer van 1956, als dirigent Alfons Deferme 
        na het concert schielijk overlijdt. Zijn opvolger wordt Henri 
        Corthouts, muziekleraar uit Koersel.</p>
        <p></div><br>
    <div class="row-fluid">
        <img class="pull-right img-polaroid" src="images/4.jpg" width="450" height="332" id="figuur4" alt="figuur4"/>
        In die vijftiger jaren wordt de fanfare ook uitgerust met een uniform. 
        Tussen 1965 en 1970 maakt de fanfare een moeilijke tijd door, vooral 
        door allerhande lokaalperikelen, waardoor soms in zeer onaangename 
        omstandigheden moet gerepeteerd worden In 1969 wordt er een nieuwe 
        bestuursploeg samengesteld. Georges Frederix neemt de taak van 
        penningmeester op zich en Andr&eacute; Claes wordt secretaris. 
        Men zoekt en vindt ook enkele niet-musicerende medewerkers om de 
        moeilijkheden beter aan te kunnen. Daaronder Jef Frederix, die 
        waarnemend voorzitter wordt. Op 10 juni 1970 wordt de feitelijke 
        vereniging die de fanfare al die jaren geweest is, omgevormd tot een 
        vereniging zonder winstoogmerk.</p>
        <p>   </div><br>
        <div class="row-fluid">
        <img class="pull-left img-polaroid" src="images/5.jpg" width="450" height="228" id="figuur5" alt="figuur5" />
        Om in leven te blijven heeft de fanfare sinds haar stichting steeds zichzelf moeten bedruipen. Een kleine jaarlijkse subsidie van de gemeente is nauwelijks voldoende om wat nieuwe muziekstukken te kopen. Voor de rest moeten door allerlei activiteiten (bals, zomerfeesten, carnavalstoeten, ophalen van erelidgelden, enzovoorts) de nodige fondsen bijeengezocht worden. In de jaren 70 wordt een nieuwe bron van inkomsten aangeboord die zeer succesrijk is : het ophalen van oud papier. Gedurende jaren wordt door muzikanten en bestuur een bijna ongelooflijke energie en uithoudingsvermogen aan de dag gelegd om het soms zeer zware karwei van papier ophalen en papier sorteren tot een goed einde te brengen. Het is pas door het instorten van de prijs voor oud papier dat aan de zeswekelijkse omhalingen een einde wordt gesteld, al blijft de fanfare tot op de dag van vandaag nog sporadisch actief op dit terrein.</p>
        <p></div><br><div class="row-fluid">
        <img class="pull-right img-polaroid" src="images/6.jpg" width="450" height="236" id="figuur6" alt="figuur6"/>
        In 1970 heeft de fanfare ook de meisjesdrumband.&QUOT;De Toekomst&QUOT;
        , door ijverige mensen uit de Veenderweg opgericht, kunnen overnemen. 
        De jonge en dynamische instructeur Domien Poelmans uit Hechtel weet op 
        enkele jaren onze meisjes van derde naar de hogere afdelingen te 
        brengen. In 1983, 1984 en 1990 behaalt de drumband de titel van 
        Kampioen van Belgi&euml;.</p>
        <p></div><br><div class="row-fluid"> 
        <img class="pull-left img-polaroid" src="images/7.jpg" width="450" height="417" id="figuur7" alt="figuur7"/>
        1977 is een jubileumjaar : met grote luister wordt het 75-jarig bestaan 
        van de fanfare gevierd. Tijdens die viering wordt Jozef Lemmens, 
        die om gezondheidsredenen de voorzitterstaak definitief overlaat 
        aan Jef Frederix, tot erevoorzitter benoemd. Het begin van de jaren 
        tachtig wordt voor de muzikanten van De Verenigde Vrienden niet 
        zozeer gekenmerkt door muzikale hoogtepunten - al blijven de 
        jaarlijkse herfst- of winterconcerten door het publiek zeer 
        gewaardeerd - maar door harde fysische arbeid: de bouw van een 
        eigen muziekzaal moet eindelijk definitief een einde maken aan 
        alle ellende rond een fatsoenlijk repetitielokaal die de fanfare 
        sinds haar ontstaan gekend heeft. Op 23 september 1983 neemt de 
        vereniging haar intrek in de nieuwe zaal, die op 15 oktober door 
        pastoor-deken Piet Moesen wordt ingezegend. Ander niet-muzikaal 
        feit uit die tijd is de uitgave, van 1978 tot 1981, van een eigen 
        tweemaandelijks tijdschrift &QUOT;Tussen Bugel en Trom&QUOT;. 
        Ook in 1984-1985 verschijnen nog vier nummers.</p>
        <p></div><br><div class="row-fluid">
        <img class="pull-right img-polaroid" src="images/8.png" width="256" height="256" id="figuur8" alt="figuur8"/>
        In 1981 wordt in het Nederlandse Heusden-Asten ook de start gegeven 
        van het evenement &QUOT;4 x Heusden&QUOT;, dat sindsdien 
        jaarlijks afwisselend in Nederland en Belgi&euml; wordt georganiseerd. 
        Er wordt aan deelgenomen door de vier muziekmaatschappijen met een 
        &QUOT;Heusden&QUOT; als thuisbasis: in Nederland de Harmonie 
        Sint-Antonius uit Heusden-Asten en de Harmonie De Eendracht 
        uit Heusden aan de Maas; in Belgi&euml; de Koninklijke Oude Gemeenteharmonie 
        uit Heusden-Destelbergen en onze eigen Koninklijke Fanfare 
        De Verenigde Vrienden uit Heusden-Zolder.</p>
        <p></div><br><div class="row-fluid">
        <img class="pull-left img-polaroid" src="images/9.jpg" width="450" height="257" id="figuur9" alt="figuur9"/>
        Na een korte periode onder leiding van Anny Mombers wordt 
        Paul Lambrichts in 1995 de nieuwe voorzitter. 
        Na gedurende 39 jaar het wel en wee van de Heusdense fanfare 
        meebeleefd te hebben neemt dirigent Henri Corthouts in 1995 
        afscheid van zijn Verenigde Vrienden. Op 15 april wordt 
        hij voor zijn jarenlange inzet gehuldigd door gemeentebestuur, 
        Fedekam en natuurlijk ook door zijn vrienden muzikanten, die hem 
        bedenken met een vergulde dirigeerstok. Met Jos Simons als dirigent 
        vangt de fanfare de laatste jaren van de twintigste eeuw aan. 
        Hij brengt een nieuwe dynamiek in de vereniging, met veel aandacht 
        voor de jongeren en de opleiding van nieuwe muzikanten. 
        Het accent verschuift hoe langer hoe meer naar de zaalconcerten, 
        terwijl straatoptredens aan belang moeten inboeten. 
        Eind 2000 geeft Jos Simons tamelijk onverwacht zijn ontslag als dirigent. 
        Zijn opvolger Guido Dreessen leidt de fanfare tijdens het 
        jubileumjaar 2002, als met een uitgebreide 
        waaier aan concerten en andere activiteiten het 
        100-jarig bestaan van de muziekvereniging gevierd wordt. 
        Bij die gelegenheid wordt ook de benaming &QUOT;fanfare&QUOT; definitief 
        opgegeven. Als Koninklijke Harmonie De Verenigde Vrienden beantwoordt 
        de nieuwe naam beter aan de werkelijke samenstelling van het korps, 
        waarin de houten blaasinstrumenten een steeds prominenter plaats 
        zijn gaan innemen. In 2004 wordt de muzikale leiding van de harmonie 
        overgenomen door dirigent Jean-Paul Hermans. Gewijzigde bezigheden 
        hopen hem midden 2006 deze functie op te geven. 
        Op 31 juli 2006 treedt Johan Martens aan als nieuwe dirigent.</p>
